    private void btnTinhLuongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhLuongActionPerformed
        if(frm == 1){
            vtl.setThuongThem(Integer.parseInt(txtThuong.getText()));
            vtl.setPhatThem(Integer.parseInt(txtPhat.getText()));
            vtl.setTongLuong(vtl.getTongLuong() + vtl.getThuongThem() - vtl.getPhatThem());
        }else{
            if(Integer.parseInt(txtThuong.getText()) != thuong){
                vtl.setThuongThem(Integer.parseInt(txtThuong.getText()));
                vtl.setTongLuong(vtl.getTongLuong() + Integer.parseInt(txtThuong.getText()) - thuong);
                
            }
            if(Integer.parseInt(txtPhat.getText()) != phat){
               
                vtl.setPhatThem(Integer.parseInt(txtPhat.getText()));
                vtl.setTongLuong(vtl.getTongLuong() - Integer.parseInt(txtPhat.getText()) + phat);
               
            }
        }
        this.dispose();
        ChiTietLuongFrm chiTietLuongFrm = new ChiTietLuongFrm(thanhVien,vtl, csThang, 2);
        
        chiTietLuongFrm.setVisible(true);
        chiTietLuongFrm.setLocationRelativeTo(null);
        
        
    }//GEN-LAST:event_btnTinhLuongActionPerformed

