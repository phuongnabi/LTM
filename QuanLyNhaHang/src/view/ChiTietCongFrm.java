    private void filltblCong(){
        ArrayList<ViecCheckInCheckOut> dsCIO = vtl.getDsCheckInOut();
        int luongTheoCong = 0, phatCICO = 0, tongCong = 0;
        int gioTangCa = 0;
        int i = 1;
        for(ViecCheckInCheckOut vcio:dsCIO){
            String ttCI="OK", ttCO ="OK";
            int luong = 0;
            CaLamViec clv = vcio.getVdkl().getCaLamViec();
            Date gioBatDau = clv.getGioBatDau();
            Date gioCI = vcio.getGioCheckIn();
            Date gioKetThuc = clv.getGioKetThuc();
            Date gioCO = vcio.getGioCheckOut();
            if(gioCI != null && gioCO != null){
                if(gioBatDau.before(gioCI)){
                    phatCICO += 10;
                    ttCI = "Muộn";
                }
                if(gioCO.before(gioKetThuc)) {
                    phatCICO += 10;
                    ttCO = "Sớm";
                }
                else{
                    gioTangCa += gioCO.getHours() - gioKetThuc.getHours();
                }
                luongTheoCong += clv.getLuongTrenCa();
                tongCong += 1;
            }
            model.addRow(new Object[]{
                i++,"Thứ "+ clv.getThu()+" "+clv.getNgay().toString(),clv.getLuongTrenCa(),clv.getGioBatDau().toString() +"->"+clv.getGioKetThuc().toString(),
                vcio.getGioCheckIn().toString(), vcio.getGioCheckOut().toString(),gioTangCa
            });
        }
        model.addRow(new Object[]{
            "","","",""
        });
        model2.addRow(new Object[]{
            "Tổng cộng: ","","Số ca làm:",tongCong,"","",""
        });
        model2.addRow(new Object[]{
            "","","Số ca phạt CICO :",phatCICO/10,"","",""
        });
        model2.addRow(new Object[]{
            "","","Số giờ tăng ca:",gioTangCa,"","",""
        });
        if(frm == 1){
            vtl.setHcns(thanhVien);
            vtl.setTienPhatCICO(phatCICO);
            vtl.setTongCong(tongCong);
            vtl.setGioTangCa(gioTangCa);
            vtl.setTienTangCa(gioTangCa*25);
            vtl.setLuongTheoCong(luongTheoCong);
            vtl.setTongLuong(luongTheoCong + gioTangCa*25 - phatCICO);
        }
        
    }

